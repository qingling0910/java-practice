package tk.amrom.day01;

public class ForTest01 {
    public static void main(String[] args) {
        System.out.println("打印1～5");
      //打印1～5
        for(int i = 1; i <= 5; i++){
            System.out.println(i);

        }
        System.out.println("打印5～1");
        for(int a = 5; a >= 1; a--){
            System.out.println(a);
        }
    }

}
