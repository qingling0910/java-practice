package tk.amrom;

public class ArithmeticOperation02 {
    public static void main(String[] agrs){
        int a = 10;
        a++;
        System.out.println(a);

        ++a;
        System.out.println(a);

        a--;
        System.out.println(a);

        --a;
        System.out.println(a);
    }
}
